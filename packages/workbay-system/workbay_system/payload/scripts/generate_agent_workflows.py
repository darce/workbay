def _expected_plugin_outputs(
    manifest: dict,
    mcp_manifest: dict,
    skills_source_root: Path,
    plugin_out: Path,
    plugin_overrides: Path | None = None,
    plugin_base_remote_sha: str | None = None,
    plugin_passthrough_lock: bool = False,
    harness_protocol: dict | None = None,
) -> tuple[dict[Path, str], list[str]]:
    """Map every emitted plugin-tree file to its rendered content.

    Layout per harness::

        <plugin_out>/<harness>/<plugin_dir>/plugin.json
        <plugin_out>/<harness>/.mcp.json   (only under `registration: plugin`)
        <plugin_out>/<harness>/skills/<slug>/SKILL.md
    """
    outputs: dict[Path, str] = {}
    audit_lines: list[str] = []
    plugin_version = mcp_manifest["plugin_version"]
    registration = mcp_manifest["registration"]
    if harness_protocol is None:
        harness_protocol = _load_harness_protocol()

    seen_slugs: list[str] = []
    rendered_skills: dict[str, str] = {}
    for command in manifest["commands"]:
        slug = command["skill"]
        if slug in rendered_skills:
            continue
        structured, body = _load_skill(skills_source_root, slug)
        rendered_skills[slug] = _render_plugin_skill(
            structured, body, manifest, harness_protocol
        )
        seen_slugs.append(slug)

    effective_components: list[dict[str, str]] = []
    coadded_skill_sources: dict[str, tuple[dict, str]] = {}
    if plugin_overrides is not None and plugin_passthrough_lock:
        raise SystemExit(
            "--plugin-passthrough-lock cannot be combined with --plugin-overrides"
        )
    if plugin_overrides is not None:
        if plugin_base_remote_sha is None:
            raise SystemExit(
                "--plugin-base-remote-sha is required when --plugin-overrides is set"
            )
        override_payload = _load_plugin_override_manifest(plugin_overrides)
        rendered_skills, seen_slugs, effective_components = (
            _compose_plugin_skill_overrides(
                rendered_skills, plugin_overrides, override_payload
            )
        )
        mcp_manifest, mcp_components, audit_lines = _compose_plugin_mcp_overrides(
            mcp_manifest,
            plugin_overrides,
            override_payload,
        )
        effective_components.extend(mcp_components)
        coadded_skill_sources = _coadded_skill_sources(
            plugin_overrides, override_payload
        )
        if override_payload.components.portable_commands:
            manifest, command_components = _compose_plugin_portable_command_overrides(
                manifest,
                plugin_overrides,
                override_payload,
                composed_skills=rendered_skills,
            )
            effective_components.extend(command_components)
            _ensure_rendered_skills_for_manifest(
                manifest,
                rendered_skills=rendered_skills,
                seen_slugs=seen_slugs,
                skills_source_root=skills_source_root,
                manifest_for_render=manifest,
                harness_protocol=harness_protocol,
                coadded_skill_sources=coadded_skill_sources,
            )
        if override_payload.components.claude_settings_hooks:
            # iter4-g: claude_settings_hooks overrides compose into the managed
            # .claude/settings.json hooks. Record each as an effective-lock
            # component (parity with skill / mcp_server / portable_command) so
            # doctor/repair can detect drift on a consumer settings-hook
            # override. The compose fn already builds the per-component digest;
            # render_composed_claude_settings_hooks previously discarded it.
            base_claude_hooks = render_claude_hooks_config(
                harness_protocol.get("hooks") or {}
            )["hooks"]
            compose = _import_plugin_override_compose()
            _composed_hooks, settings_hook_components = (
                compose.compose_claude_settings_hooks_overrides(
                    base_claude_hooks,
                    plugin_overrides,
                    override_payload,
                )
            )
            effective_components.extend(settings_hook_components)
    elif plugin_passthrough_lock:
        # internal always-effective: a zero-override effective tree is the base
        # tree composed unchanged; the lock receipt records every component as
        # passthrough so doctor/repair can distinguish "composed clean" from
        # "never composed".
        if plugin_base_remote_sha is None:
            raise SystemExit(
                "--plugin-base-remote-sha is required when --plugin-passthrough-lock is set"
            )
        effective_components = [
            {
                "component_kind": "skill",
                "name": slug,
                "mode": "passthrough",
                "effective_digest": _sha256_digest(rendered_skills[slug]),
            }
            for slug in seen_slugs
        ]
        effective_components.extend(
            {
                "component_kind": "mcp_server",
                "name": server["name"],
                "mode": "passthrough",
                "effective_digest": _canonical_mcp_server_digest(server),
            }
            for server in mcp_manifest["mcp_servers"]
        )
        effective_components.extend(
            {
                "component_kind": "portable_command",
                "name": command["command_id"],
                "mode": "passthrough",
                "effective_digest": _portable_command_digest(command),
            }
            for command in manifest["commands"]
        )

    grok_hooks_body = _render_grok_plugin_hooks_json(harness_protocol)
    cursor_hooks_body = render_hooks_config_text(
        render_cursor_hooks_config(harness_protocol.get("hooks") or {})
    )

    for harness, plugin_dir in _PLUGIN_HARNESSES:
        harness_root = plugin_out / harness
        plugin_owns_mcp = registration[harness] == "plugin"
        outputs[harness_root / plugin_dir / "plugin.json"] = (
            _render_plugin_manifest_json(
                plugin_version, include_mcp_servers=plugin_owns_mcp
            )
        )
        if plugin_owns_mcp:
            outputs[harness_root / ".mcp.json"] = _render_mcp_servers_json(
                mcp_manifest, harness
            )
        if harness == "grok":
            outputs[harness_root / "hooks" / "hooks.json"] = grok_hooks_body
        if harness == "cursor":
            outputs[harness_root / "hooks.json"] = cursor_hooks_body
            # Skills-only surface: a single marker in place of per-command
            # markdown so Cursor does not show duplicate workflow picker entries.
            outputs[harness_root / "commands" / _CURSOR_SKILLS_ONLY_MARKER_NAME] = (
                _CURSOR_COMMANDS_SKILLS_ONLY_MARKER
            )
        for slug in seen_slugs:
            outputs[harness_root / "skills" / slug / "SKILL.md"] = rendered_skills[slug]
    if plugin_overrides is not None or plugin_passthrough_lock:
        outputs[plugin_out / _PLUGIN_EFFECTIVE_LOCK] = (
            _render_plugin_effective_lock_json(
                plugin_base_remote_sha,
                plugin_out,
                effective_components,
            )
        )
    return outputs, audit_lines


def _extra_plugin_tree_files(plugin_out: Path, outputs: dict[Path, str]) -> list[Path]:
    if not plugin_out.exists():
        return []
    expected = {path.resolve() for path in outputs}
    return sorted(
        path
        for path in plugin_out.rglob("*")
        if path.is_file() and path.resolve() not in expected
    )


def _prune_plugin_tree(plugin_out: Path, outputs: dict[Path, str]) -> None:
    for path in _extra_plugin_tree_files(plugin_out, outputs):
        path.unlink()
    if not plugin_out.exists():
        return
    for path in sorted(
        plugin_out.rglob("*"), key=lambda item: len(item.parts), reverse=True
    ):
        if not path.is_dir():
            continue
        try:
            path.rmdir()
        except OSError:
            continue


def _emit_plugin_tree(plugin_out: Path, outputs: dict[Path, str]) -> None:
    """Write every plugin-tree file deterministically."""
    _prune_plugin_tree(plugin_out, outputs)
    for path, content in sorted(outputs.items()):
        path.parent.mkdir(parents=True, exist_ok=True)
        path.write_text(content)


def _check_plugin_outputs(plugin_out: Path, outputs: dict[Path, str]) -> list[str]:
    failures: list[str] = []
    for path, expected in sorted(outputs.items()):
        if not path.exists():
            failures.append(f"missing plugin tree file: {path}")
            continue
        if path.read_text() != expected:
            failures.append(f"plugin tree drift: {path}")
    for path in _extra_plugin_tree_files(plugin_out, outputs):
        failures.append(f"unexpected plugin tree file: {path}")
    return failures


def _run_plugin_mode(
    args: argparse.Namespace, package_root: Path, manifest: dict
) -> int:
    skills_source_root = (
        args.skills_source_root or args.claude_skills_root or (package_root / "skills")
    )
    mcp_servers_path = args.plugin_mcp_servers or (
        package_root / "config" / "agent-workflows" / "mcp_servers.yaml"
    )
    if args.plugin_out is None:
        raise SystemExit("--mode=plugin requires --plugin-out <dest>")
    mcp_manifest = _load_mcp_servers_manifest(mcp_servers_path)
    outputs, audit_lines = _expected_plugin_outputs(
        manifest,
        mcp_manifest,
        skills_source_root,
        args.plugin_out,
        args.plugin_overrides,
        args.plugin_base_remote_sha,
        args.plugin_passthrough_lock,
    )
    stale_components: list[dict[str, object]] = []
    conflict_components: list[dict[str, object]] = []
    lock_payload = outputs.get(args.plugin_out / _PLUGIN_EFFECTIVE_LOCK)
    if lock_payload is not None:
        try:
            parsed_lock = json.loads(lock_payload)
        except json.JSONDecodeError:
            parsed_lock = {}
        stale_components = [
            entry
            for entry in parsed_lock.get("components", [])
            if isinstance(entry, dict) and entry.get("status") == "stale"
        ]
        conflict_components = [
            entry
            for entry in parsed_lock.get("components", [])
            if isinstance(entry, dict) and entry.get("status") == "merge_conflict"
        ]
    if args.check:
        failures = _check_plugin_outputs(args.plugin_out, outputs)
        if stale_components:
            print("Plugin override composition has stale overrides:", file=sys.stderr)
            for entry in stale_components:
                print(
                    "  - stale override: "
                    f"{entry.get('override_path')} "
                    f"(recorded {entry.get('recorded_upstream_digest')} vs current {entry.get('current_base_digest')})",
                    file=sys.stderr,
                )
        if conflict_components:
            print("Plugin override composition has merge conflicts:", file=sys.stderr)
            for entry in conflict_components:
                print(
                    "  - merge conflict: "
                    f"{entry.get('override_path')} "
                    f"(recorded {entry.get('recorded_upstream_digest')} vs current {entry.get('current_base_digest')})",
                    file=sys.stderr,
                )
        if failures:
            print(
                "Plugin tree is out of sync with the canonical inputs:", file=sys.stderr
            )
            for failure in failures:
                print(f"  - {failure}", file=sys.stderr)
        if failures or stale_components or conflict_components:
            return 1
        print(f"✓ plugin-tree: {len(outputs)} files match canonical inputs.")
        return 0
    _emit_plugin_tree(args.plugin_out, outputs)
    for line in audit_lines:
        print(line)
    if stale_components:
        for entry in stale_components:
            print(
                "warning: stale override preserved: "
                f"{entry.get('override_path')} "
                f"(recorded {entry.get('recorded_upstream_digest')} vs current {entry.get('current_base_digest')})",
                file=sys.stderr,
            )
    if conflict_components:
        for entry in conflict_components:
            print(
                "warning: merge conflict; consumer copy preserved: "
                f"{entry.get('override_path')} "
                f"(recorded {entry.get('recorded_upstream_digest')} vs current {entry.get('current_base_digest')})",
                file=sys.stderr,
            )
    print(f"✓ plugin-tree: wrote {len(outputs)} files under {args.plugin_out}.")
    return 0


def render_plugin_tree(
    *,
    manifest_path: Path,
    skills_source_root: Path,
    mcp_servers_path: Path,
    plugin_out: Path,
    plugin_overrides: Path | None = None,
    plugin_base_remote_sha: str | None = None,
    plugin_passthrough_lock: bool = False,
) -> dict[Path, str]:
    """Single importable entry point for the plugin-tree payload (path -> content).

    implementation note S2 (pa0020-s2-build-vs-install-generation-parity): every adapter
    producer routes through the same ``_expected_plugin_outputs`` core so build
    output == install output for the same inputs —
      * the build hook / ``make generate-agent-workflows`` calls this directly
        in-process, and
      * the install-time generator in ``workbay-bootstrap`` reaches the same
        core via the subprocess CLI (``main`` -> ``_run_plugin_mode``).

    Pure: computes the output map without writing to disk, so a caller can
    render-then-decide (diff against committed, emit, or ``--check``) before
    any write lands. Materialise the returned map with ``_emit_plugin_tree``.
    """
    manifest = _load_manifest(manifest_path)
    mcp_manifest = _load_mcp_servers_manifest(mcp_servers_path)
    outputs, _audit_lines = _expected_plugin_outputs(
        manifest,
        mcp_manifest,
        skills_source_root,
        plugin_out,
        plugin_overrides,
        plugin_base_remote_sha,
        plugin_passthrough_lock,
    )
    return outputs


def main() -> int:
    args = _parse_args()
    if args.check_mcp_pins:
        return check_mcp_pin_parity(args.manifest, args.pin_source)
    manifest = _load_manifest(args.manifest)
    package_root = args.manifest.resolve().parents[2]

    if args.mode == "plugin":
        return _run_plugin_mode(args, package_root, manifest)

    # Source-of-truth: skills/<slug>/{skill.yaml, body.md}.
    # --claude-skills-root is the legacy alias kept for one release.
    skills_source_root = (
        args.skills_source_root or args.claude_skills_root or (package_root / "skills")
    )

    # Per-agent output paths. --target is sugar for "set every per-agent
    # output to this consumer repo's standard location"; explicit
    # per-agent flags still override.
    target = args.target
    claude_out = args.claude_out or (
        target / ".claude" / "commands" if target else None
    )
    claude_skills_out = args.claude_skills_out or (
        target / ".claude" / "skills" if target else None
    )
    prompts_out = args.prompts_out or (
        target / ".github" / "prompts" if target else None
    )
    codex_router_out = args.codex_router_out or (
        target.joinpath(*_GENERATED_DOCS_SUBDIR) if target else None
    )
    codex_skills_out = args.codex_skills_out or (
        target / ".codex" / "skills" if target else None
    )

    # When invoked without --target and without explicit out paths
    # (e.g. the workbay-system source-of-truth regen) the generated adapters
    # fall back to the package-internal directory that lives OUTSIDE the shipped
    # payload. implementation note S3 moved the generator under workbay_system/payload/,
    # so `package_root` now resolves to the payload tree itself — but generated
    # adapters are internal artifacts that must never land under payload (the
    # tests/test_payload_colocation.py privacy guard forbids `/generated` and the
    # wheel ships payload/ wholesale). Anchor them at the package dir two levels
    # up (payload/ -> workbay_system/ -> packages/workbay-system) so the
    # tracked docs/workbay/generated/codex-command-router.md heals in place and
    # the gitignored .github/prompts/ adapters regenerate cleanly.
    internal_root = package_root.parents[1]
    if claude_out is None:
        claude_out = internal_root / ".claude" / "commands"
    if prompts_out is None:
        prompts_out = internal_root / ".github" / "prompts"
    if codex_router_out is None:
        codex_router_out = internal_root.joinpath(*_GENERATED_DOCS_SUBDIR)

    coadded_skill_sources: dict[str, tuple[dict, str]] = {}
    if args.plugin_overrides is not None:
        override_payload = _load_plugin_override_manifest(args.plugin_overrides)
        # adapter_rendered_skills is a resolution GATE only: it exists so the
        # portable-command composer can confirm each command's skill resolves
        # (upstream or co-added). It is discarded after composition and is never
        # an emission source — SKILL.md output is owned by the plugin tree — so
        # the 3-arg _render_plugin_skill (no harness_protocol) is sufficient
        # here and does not need to match _expected_plugin_outputs' 4-arg form.
        adapter_rendered_skills: dict[str, str] = {}
        for command in manifest["commands"]:
            slug = command["skill"]
            if slug in adapter_rendered_skills:
                continue
            structured, body = _load_skill(skills_source_root, slug)
            adapter_rendered_skills[slug] = _render_plugin_skill(
                structured, body, manifest
            )
        adapter_rendered_skills, _seen_slugs, _components = (
            _compose_plugin_skill_overrides(
                adapter_rendered_skills,
                args.plugin_overrides,
                override_payload,
            )
        )
        if override_payload.components.portable_commands:
            manifest, _command_components = _compose_plugin_portable_command_overrides(
                manifest,
                args.plugin_overrides,
                override_payload,
                composed_skills=adapter_rendered_skills,
            )
        coadded_skill_sources = _coadded_skill_sources(
            args.plugin_overrides, override_payload
        )

    outputs = _expected_outputs(
        manifest,
        skills_source_root,
        claude_out,
        prompts_out,
        codex_router_out,
        coadded_skill_sources,
    )
    skill_outputs = _expected_skill_outputs(
        manifest, skills_source_root, claude_skills_out, codex_skills_out
    )

    # repo_root for the codex-router-consumers sync is the target repo
    # if --target was given, otherwise the manifest-inferred package root.
    repo_root = target if target is not None else package_root
    hooks_outputs = _expected_hooks_outputs(repo_root)

    if args.check_coherence:
        try:
            from workbay_bootstrap.coherence import main as coherence_main  # type: ignore[import-not-found]
        except ImportError as exc:
            print(
                "--check-coherence requires workbay-bootstrap to be importable",
                file=sys.stderr,
            )
            print(f"import error: {exc}", file=sys.stderr)
            return 1
        return int(coherence_main([str(repo_root)]))

    if args.check_codex_router_blocks:
        failures = _sync_codex_router_consumers(manifest, repo_root, check=True)
        if failures:
            print(
                "Codex router consumer docs are out of sync with the manifest:",
                file=sys.stderr,
            )
            for failure in failures:
                print(f"  - {failure}", file=sys.stderr)
            return 1
        print("✓ codex-router: consumer docs match the manifest-rendered block.")
        return 0

    if args.check:
        exit_code = _check_outputs(outputs, claude_out, prompts_out, codex_router_out)
        skill_failures = _check_skill_outputs(skill_outputs)
        if skill_failures:
            print(
                "Generated skill files are out of sync with the manifest:",
                file=sys.stderr,
            )
            for failure in skill_failures:
                print(f"  - {failure}", file=sys.stderr)
            exit_code = 1
        hooks_failures = [
            f"hook config drift: {path}"
            for path, expected in hooks_outputs.items()
            if not path.exists() or path.read_text() != expected
        ]
        if hooks_failures:
            print(
                "Generated hook configs are out of sync with harness-protocol.yaml:",
                file=sys.stderr,
            )
            for failure in hooks_failures:
                print(f"  - {failure}", file=sys.stderr)
            exit_code = 1
        return exit_code

    _write_outputs(outputs)
    _write_outputs(hooks_outputs)
    failures = _sync_codex_router_consumers(
        manifest, repo_root, check=False, onboard=args.onboard_router_markers
    )
    if failures:
        raise SystemExit("\n".join(failures))
    _write_skill_files(skill_outputs)
    expected_slugs = {command["skill"] for command in manifest["commands"]}
    _prune_stale_skill_dirs(claude_skills_out, expected_slugs)
    _prune_stale_skill_dirs(codex_skills_out, expected_slugs)
    _prune_legacy_portable_emissions(
        manifest, claude_out, claude_skills_out, codex_skills_out
    )
    total = len(outputs) + len(skill_outputs) + len(hooks_outputs)
    print(f"✓ agent-workflows: wrote {total} adapter files.")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
