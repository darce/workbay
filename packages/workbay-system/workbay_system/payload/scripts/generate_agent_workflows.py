_MCP_PIN_LITERAL_RE = re.compile(
    r"mcp-workbay-[a-z-]+(?:\[bridge\])?@[0-9]+(?:\.[0-9]+)*"
)
_PIN_SOURCE_VAR_RE = re.compile(
    r"^MCP_[A-Z0-9_]+_PACKAGE\s*[:?]?=\s*(" + _MCP_PIN_LITERAL_RE.pattern + r")",
    re.MULTILINE,
)


def _extract_mcp_pin_literals(text: str) -> dict[str, str]:
    """Map distribution name (before @) to full pinned literal."""
    pins: dict[str, str] = {}
    for match in _MCP_PIN_LITERAL_RE.finditer(text):
        literal = match.group(0)
        dist = literal.split("@", 1)[0]
        pins[dist] = literal
    return pins


def _mcp_servers_yaml_path(manifest_path: Path) -> Path:
    package_root = manifest_path.resolve().parents[2]
    return package_root / "config" / "agent-workflows" / "mcp_servers.yaml"


def check_mcp_pin_parity(manifest_path: Path, pin_source: Path | None) -> int:
    """Consumer-opt-in overlay pin check. No-ops when pin_source is absent."""
    if pin_source is None:
        return 0
    if not pin_source.is_file():
        print(f"ERROR: --pin-source not found: {pin_source}", file=sys.stderr)
        return 1
    yaml_path = _mcp_servers_yaml_path(manifest_path)
    if not yaml_path.is_file():
        print(f"ERROR: mcp_servers.yaml not found: {yaml_path}", file=sys.stderr)
        return 1
    source_text = pin_source.read_text(encoding="utf-8")
    source_pins = {
        match.group(1).split("@", 1)[0]: match.group(1)
        for match in _PIN_SOURCE_VAR_RE.finditer(source_text)
    }
    yaml_pins = _extract_mcp_pin_literals(yaml_path.read_text(encoding="utf-8"))
    errors: list[str] = []
    for dist, yaml_pin in sorted(yaml_pins.items()):
        source_pin = source_pins.get(dist)
        if source_pin is None:
            continue
        if source_pin != yaml_pin:
            errors.append(
                f"{dist}: pin-source {source_pin!r} != mcp_servers.yaml {yaml_pin!r}"
            )
    if errors:
        for line in errors:
            print(f"ERROR: {line}", file=sys.stderr)
        return 1
    return 0


def _load_manifest(path: Path) -> dict:
    try:
        payload = json.loads(path.read_text())
    except FileNotFoundError as exc:
        raise SystemExit(f"Manifest not found: {path}") from exc
    except json.JSONDecodeError as exc:
        raise SystemExit(f"Invalid JSON in {path}: {exc}") from exc

    if not isinstance(payload, dict):
        raise SystemExit(f"{path} must contain a JSON object.")

    version = payload.get("version")
    if version not in {1, 2}:
        raise SystemExit(
            f"{path} must declare version=1 or version=2; found {version!r}."
        )

    commands = payload.get("commands")
    if not isinstance(commands, list) or not commands:
        raise SystemExit(f"{path} must declare a non-empty commands list.")

    global_instructions = payload.get("global_instructions", [])
    if global_instructions != [] and (
        not isinstance(global_instructions, list)
        or not all(
            isinstance(item, str) and item.strip() for item in global_instructions
        )
    ):
        raise SystemExit(
            f"{path} must declare global_instructions as a list of non-empty strings when present."
        )

    seen_ids: set[str] = set()
    for index, command in enumerate(commands):
        _validate_command(command, index)
        command_id = command["command_id"]
        if command_id in seen_ids:
            raise SystemExit(f"Duplicate command_id {command_id!r} in {path}.")
        seen_ids.add(command_id)

    hooks = payload.get("hooks", [])
    if hooks != [] and not isinstance(hooks, list):
        raise SystemExit(f"{path} must declare hooks as a list when present.")
    for index, hook in enumerate(hooks):
        _validate_hook(hook, index)
    return payload


def _validate_command(command: object, index: int) -> None:
    if not isinstance(command, dict):
        raise SystemExit(f"commands[{index}] must be an object.")

    required_string_fields = (
        "command_id",
        "skill",
        "makefile_target",
        "description",
        "execution_context",
    )
    for field in required_string_fields:
        value = command.get(field)
        if not isinstance(value, str) or not value.strip():
            raise SystemExit(f"commands[{index}].{field} must be a non-empty string.")

    command_id = command["command_id"]
    if any(ch not in "abcdefghijklmnopqrstuvwxyz0123456789-" for ch in command_id):
        raise SystemExit(
            f"commands[{index}].command_id must be lowercase kebab-case; found {command_id!r}."
        )

    # internal1: declare-or-fail mode field. The portable-commands
    # interaction-mode taxonomy is the spec contract between MCP-writer
    # skills and consumer routing; every command must pick one of three.
    mode = command.get("mode")
    if mode is None:
        raise SystemExit(
            f"commands[{index}].mode is required; declare one of "
            f"'guide', 'verify', 'write'."
        )
    if mode not in _VALID_COMMAND_MODES:
        raise SystemExit(
            f"commands[{index}].mode must be one of "
            f"{sorted(_VALID_COMMAND_MODES)!r}; found mode={mode!r}."
        )

    loop = command.get("loop")
    if (
        not isinstance(loop, list)
        or not loop
        or not all(isinstance(item, str) and item.strip() for item in loop)
    ):
        raise SystemExit(f"commands[{index}].loop must be a non-empty list of strings.")

    secondary_targets = command.get("secondary_targets", [])
    if secondary_targets != [] and (
        not isinstance(secondary_targets, list)
        or not all(isinstance(item, str) and item.strip() for item in secondary_targets)
    ):
        raise SystemExit(
            f"commands[{index}].secondary_targets must be a list of strings when present."
        )

    argument_schema = command.get("argument_schema")
    if not isinstance(argument_schema, list):
        raise SystemExit(f"commands[{index}].argument_schema must be a list.")
    for arg_index, arg in enumerate(argument_schema):
        if not isinstance(arg, dict):
            raise SystemExit(
                f"commands[{index}].argument_schema[{arg_index}] must be an object."
            )
        name = arg.get("name")
        description = arg.get("description")
        required = arg.get("required")
        if not isinstance(name, str) or not name.strip():
            raise SystemExit(
                f"commands[{index}].argument_schema[{arg_index}].name must be a non-empty string."
            )
        if not isinstance(description, str) or not description.strip():
            raise SystemExit(
                f"commands[{index}].argument_schema[{arg_index}].description must be a non-empty string."
            )
        if not isinstance(required, bool):
            raise SystemExit(
                f"commands[{index}].argument_schema[{arg_index}].required must be boolean."
            )


def _validate_hook(hook: object, index: int) -> None:
    if not isinstance(hook, dict):
        raise SystemExit(f"hooks[{index}] must be an object.")

    required_string_fields = ("hook_id", "description", "trigger")
    for field in required_string_fields:
        value = hook.get(field)
        if not isinstance(value, str) or not value.strip():
            raise SystemExit(f"hooks[{index}].{field} must be a non-empty string.")

    required_artifacts = hook.get("required_artifacts")
    if not isinstance(required_artifacts, list) or not required_artifacts:
        raise SystemExit(f"hooks[{index}].required_artifacts must be a non-empty list.")
    adapters = hook.get("adapters")
    if not isinstance(adapters, list) or not adapters:
        raise SystemExit(f"hooks[{index}].adapters must be a non-empty list.")
    for adapter_index, adapter in enumerate(adapters):
        _validate_hook_adapter(adapter, index, adapter_index)


def _validate_hook_adapter(
    adapter: object, hook_index: int, adapter_index: int
) -> None:
    prefix = f"hooks[{hook_index}].adapters[{adapter_index}]"
    if not isinstance(adapter, dict):
        raise SystemExit(f"{prefix} must be an object.")
    harness = adapter.get("harness")
    if not isinstance(harness, str) or not harness.strip():
        raise SystemExit(f"{prefix}.harness must be a non-empty string.")
    patch = adapter.get("patch")
    if not isinstance(patch, dict):
        raise SystemExit(f"{prefix}.patch must be an object.")
    entry = patch.get("entry")
    if not isinstance(entry, dict):
        raise SystemExit(f"{prefix}.patch.entry must be an object.")

    if harness in {"claude-code", "grok"}:
        nested_hooks = entry.get("hooks")
        if not isinstance(nested_hooks, list) or not nested_hooks:
            raise SystemExit(
                f"{prefix}.patch.entry must use the matcher + hooks[] shape "
                f"required by {harness}."
            )
        matcher = entry.get("matcher")
        if not isinstance(matcher, str):
            raise SystemExit(f"{prefix}.patch.entry.matcher must be a string.")
        for hook_entry_index, hook_entry in enumerate(nested_hooks):
            if not isinstance(hook_entry, dict):
                raise SystemExit(
                    f"{prefix}.patch.entry.hooks[{hook_entry_index}] must be an object."
                )
            if hook_entry.get("type") != "command":
                raise SystemExit(
                    f"{prefix}.patch.entry.hooks[{hook_entry_index}].type must be 'command'."
                )
            command = hook_entry.get("command")
            if not isinstance(command, str) or not command.strip():
                raise SystemExit(
                    f"{prefix}.patch.entry.hooks[{hook_entry_index}].command must be a non-empty string."
                )
    else:
        # codex/vscode adapters use the flat entry shape; validate the
        # command symmetrically with the nested claude shape above.
        command = entry.get("command")
        if not isinstance(command, str) or not command.strip():
            raise SystemExit(
                f"{prefix}.patch.entry.command must be a non-empty string."
            )


def _render_arguments(command: dict) -> list[str]:
    args = command.get("argument_schema", [])
    if not args:
        return ["Arguments: none."]
    lines = ["Arguments:"]
    for arg in args:
        status = "required" if arg["required"] else "optional"
        lines.append(f"- `{arg['name']}` ({status}): {arg['description']}")
    return lines


def _render_targets(command: dict) -> list[str]:
    lines = [f"Makefile entry point: `{command['makefile_target']}`"]
    secondary = command.get("secondary_targets", [])
    if secondary:
        lines.append("Secondary entry points:")
        lines.extend(f"- `{target}`" for target in secondary)
    return lines


def _global_instruction_items(manifest: dict) -> list[str]:
    instructions = manifest.get("global_instructions") or []
    return list(instructions)


def _global_instruction_bullets(manifest: dict) -> list[str]:
    return [f"- {item}" for item in _global_instruction_items(manifest)]


def _render_global_instructions(manifest: dict) -> list[str]:
    bullets = _global_instruction_bullets(manifest)
    if not bullets:
        return []
    return [
        "Global instructions:",
        "",
        *bullets,
        "",
    ]


def _render_prompt_with_guidance(
    command: dict, prompt_guidance: str | None, manifest: dict
) -> str:
    lines = [
        GENERATOR_TAG.rstrip(),
        "",
        PROMPT_HEADER.format(command_id=command["command_id"]).rstrip(),
        "",
        *_render_global_instructions(manifest),
        f"Load the `{command['skill']}` skill for this workflow.",
        "",
        f"Description: {command['description']}",
        "",
        f"Mode: `{command['mode']}`",
        "",
        *_render_targets(command),
        "",
        *_render_arguments(command),
        "",
        f"Execution context: {command['execution_context']}",
        "",
        "Loop:",
        *(f"- {item}" for item in command["loop"]),
        "",
    ]
    if prompt_guidance:
        lines.extend(
            [
                "Canonical Skill Guidance:",
                "",
                *prompt_guidance.rstrip().splitlines(),
                "",
            ]
        )
    return "\n".join(lines)


def _extract_markdown_section(body: str, heading: str) -> str | None:
    lines = body.splitlines()
    marker = f"## {heading}"
    start: int | None = None

    for index, line in enumerate(lines):
        if line.strip() == marker:
            start = index
            break

    if start is None:
        return None

    end = len(lines)
    for index in range(start + 1, len(lines)):
        if lines[index].startswith("## "):
            end = index
            break

    return "\n".join(lines[start:end]).strip()


def _extract_prompt_guidance(structured: dict, body: str) -> str | None:
    _, generator = _strip_generator_metadata(structured)
    requested_sections = generator.get("prompt_body_sections") or []
    if not isinstance(requested_sections, list):
        requested_sections = []

    # VS Code Copilot does not consume the Claude/Codex plugin tree, so
    # its generated prompt must carry the canonical skill implementation
    # by default. Individual skills can keep prompts smaller by declaring
    # generator.prompt_body_sections in skill.yaml.
    if not requested_sections:
        return body.strip() or None

    sections: list[str] = []
    for heading in requested_sections:
        if not isinstance(heading, str) or not heading.strip():
            continue
        section = _extract_markdown_section(body, heading.strip())
        if section:
            sections.append(section)

    if not sections:
        return None
    return "\n\n".join(sections)


