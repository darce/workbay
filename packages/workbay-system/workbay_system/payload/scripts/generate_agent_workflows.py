def _write_skill_files(outputs: dict[Path, str]) -> None:
    """Write generated skill files; replace pre-existing symlinks
    pointing at a former canonical source.
    """
    for path, content in outputs.items():
        path.parent.mkdir(parents=True, exist_ok=True)
        # If the parent directory used to be a symlink (legacy layout
        # where .codex/skills/<slug> linked to .claude/skills/<slug>),
        # replace it with a real directory.
        if path.parent.is_symlink():
            target = path.parent
            target.unlink()
            target.mkdir(parents=True, exist_ok=True)
        if path.is_symlink():
            path.unlink()
        # Codex output was previously emitted as lowercase skill.md.
        # On case-insensitive filesystems writing SKILL.md over an
        # existing skill.md preserves the lowercase casing, which
        # breaks Codex skill discovery. Remove any legacy lowercase
        # sibling before writing so the on-disk name is exactly SKILL.md.
        if path.name == "SKILL.md":
            legacy = path.with_name("skill.md")
            if legacy.is_symlink() or legacy.exists():
                legacy.unlink()
        path.write_text(content)


def _prune_stale_skill_dirs(out_root: Path | None, expected_slugs: set[str]) -> None:
    """Remove generated skill directories whose slug is no longer in the manifest."""
    if out_root is None or not out_root.exists():
        return
    for entry in out_root.iterdir():
        if entry.name in expected_slugs:
            continue
        if entry.is_symlink() or entry.is_file():
            entry.unlink()
        else:
            shutil.rmtree(entry)


def _prune_legacy_portable_emissions(
    manifest: dict,
    claude_commands_out: Path | None,
    claude_skills_out: Path | None,
    codex_skills_out: Path | None,
) -> list[Path]:
    """Remove legacy emissions for portable command_ids / skill slugs.

    internal stopped writing `.claude/commands/<id>.md`,
    `.claude/skills/<slug>/SKILL.md`, and `.codex/skills/<slug>/SKILL.md`
    for portable manifest entries; those surfaces are owned by the
    plugin tree now. ``_prune_stale_skill_dirs`` only removes slugs that
    were dropped from the manifest, so pre-existing legacy files for
    *still-portable* slugs would otherwise persist as duplicates of the
    plugin-tree content.

    Deletion is conservative:
      - ``.claude/commands/<id>.md``: only removed when the file starts
        with ``GENERATOR_TAG`` (same gate as ``_extra_generated_files``);
        hand-authored slash commands at the same id are left alone.
      - ``.claude/skills/<slug>/SKILL.md`` and ``.codex/skills/<slug>/SKILL.md``
        (plus the legacy lowercase ``skill.md`` sibling): removed only
        when the file's frontmatter ``name`` matches the portable slug,
        which is the shape every pre-cutover legacy emission has (the
        legacy generator copied ``skills/<slug>/body.md`` whose canonical
        frontmatter declares ``name: <slug>``). A file with a non-matching
        ``name`` is treated as a hand-authored project-local override and
        left alone — this is the BR-04 safety gate that keeps the prune
        helper from clobbering contributor files at colliding paths.
      - The parent slug directory is removed only when it becomes empty
        after the SKILL.md deletion, so user-added siblings are
        preserved.
    """
    removed: list[Path] = []
    for command in manifest["commands"]:
        command_id = command["command_id"]
        slug = command["skill"]
        if claude_commands_out is not None:
            legacy_command = claude_commands_out / f"{command_id}.md"
            if legacy_command.is_file():
                try:
                    if legacy_command.read_text().startswith(GENERATOR_TAG):
                        legacy_command.unlink()
                        removed.append(legacy_command)
                except OSError:
                    pass
        for skills_root in (claude_skills_out, codex_skills_out):
            if skills_root is None:
                continue
            skill_dir = skills_root / slug
            if not skill_dir.exists():
                continue
            for name in ("SKILL.md", "skill.md"):
                candidate = skill_dir / name
                if candidate.is_symlink() or candidate.is_file():
                    if _legacy_skill_matches_portable_slug(candidate, slug):
                        candidate.unlink()
                        removed.append(candidate)
            try:
                if not any(skill_dir.iterdir()):
                    skill_dir.rmdir()
                    removed.append(skill_dir)
            except OSError:
                pass
    return removed


def _legacy_skill_matches_portable_slug(path: Path, slug: str) -> bool:
    """Return True iff ``path`` looks like a pre-cutover legacy SKILL.md
    emission for ``slug`` — i.e. it has YAML frontmatter whose ``name``
    field equals the slug. A file at a portable slug path that declares a
    different ``name`` (or has no parseable frontmatter) is treated as a
    hand-authored project-local override and preserved.
    """
    try:
        text = path.read_text()
    except OSError:
        return False
    if not text.startswith("---\n"):
        return False
    try:
        _, fm_raw, _ = text.split("---\n", 2)
    except ValueError:
        return False
    try:
        frontmatter = yaml.safe_load(fm_raw) or {}
    except yaml.YAMLError:
        return False
    if not isinstance(frontmatter, dict):
        return False
    return frontmatter.get("name") == slug


def _check_skill_outputs(outputs: dict[Path, str]) -> list[str]:
    failures: list[str] = []
    for path, expected in outputs.items():
        if not path.exists():
            failures.append(f"missing generated skill file: {path}")
            continue
        if path.is_symlink():
            failures.append(
                f"generated skill path is a symlink (legacy layout): {path}"
            )
            continue
        # Case-insensitive filesystems resolve Path('SKILL.md') to an
        # on-disk skill.md, hiding stale lowercase emissions from the
        # drift gate. Codex skill discovery is case-sensitive, so verify
        # the actual directory entry name matches the expected casing.
        if path.parent.is_dir() and path.name not in {
            entry.name for entry in path.parent.iterdir()
        }:
            failures.append(
                f"generated skill file has wrong on-disk casing: {path} "
                f"(expected exact name {path.name!r})"
            )
            continue
        actual = path.read_text()
        if actual != expected:
            failures.append(f"drift detected: {path}")
    return failures


