def _parse_skill_md(text: str, *, source: str) -> tuple[dict, str]:
    if not text.startswith("---"):
        raise SystemExit(f"{source}: SKILL.md must begin with YAML frontmatter")
    parts = text.split("---", 2)
    if len(parts) < 3:
        raise SystemExit(f"{source}: SKILL.md frontmatter is malformed")
    structured = yaml.safe_load(parts[1]) or {}
    if not isinstance(structured, dict):
        raise SystemExit(f"{source}: SKILL.md frontmatter must parse to a mapping")
    return structured, parts[2].lstrip("\n")


def _coadded_skill_sources(
    override_root: Path, payload: PluginOverrideManifest
) -> dict[str, tuple[dict, str]]:
    sources: dict[str, tuple[dict, str]] = {}
    for slug, override in payload.components.skills.items():
        if override.mode != "add" or not override.path:
            continue
        path = override_root / override.path
        sources[slug] = _parse_skill_md(path.read_text(), source=str(path))
    return sources


def _ensure_rendered_skills_for_manifest(
    manifest: dict,
    *,
    rendered_skills: dict[str, str],
    seen_slugs: list[str],
    skills_source_root: Path,
    manifest_for_render: dict,
    harness_protocol: dict | None,
    coadded_skill_sources: dict[str, tuple[dict, str]],
) -> None:
    for command in manifest["commands"]:
        slug = command["skill"]
        if slug in rendered_skills:
            continue
        # Defensive fallback: a co-added skill normally lands in rendered_skills
        # via _compose_plugin_skill_overrides before this runs, so this branch
        # is only reached if a command references a co-added skill that bypassed
        # skill composition (none do today). Kept so command->skill linkage can
        # never emit a command whose skill was never rendered.
        if slug in coadded_skill_sources:
            structured, body = coadded_skill_sources[slug]
            rendered_skills[slug] = _render_plugin_skill(
                structured, body, manifest_for_render, harness_protocol
            )
            seen_slugs.append(slug)
            continue
        structured, body = _load_skill(skills_source_root, slug)
        rendered_skills[slug] = _render_plugin_skill(
            structured, body, manifest_for_render, harness_protocol
        )
        seen_slugs.append(slug)


