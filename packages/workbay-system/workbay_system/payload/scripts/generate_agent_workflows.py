# --- per-harness hook configs (internal) --------------------------------
#
# Single source: the `hooks:` section of docs/workbay/contracts/
# harness-protocol.yaml. The per-harness JSON configs are generated goldens:
#   .github/hooks/terminal-guard.json  (VS Code / Copilot runner)
#   .codex/hooks.json                  (Codex CLI)
# Codex handlers carry `statusMessage` (rendered from the contract's
# harness-neutral `status_message`) so its UI names hooks instead of showing
# "Hook 1 / Hook 2"; the VS Code runner has no display field yet, so the value
# is omitted there. The contract-derived Claude hooks block is generated as
# ``.claude/settings.hooks.json`` (hooks subtree only); wholesale
# ``.claude/settings.json`` composition lands in internal.

_HOOKS_CONTRACT_RELATIVE = HARNESS_CONTRACT_RELPATH  # SSOT (workbay_protocol.paths)
_HOOK_STAGE_KEYS: tuple[tuple[str, str], ...] = (
    ("pre_tool_use", "PreToolUse"),
    ("post_tool_use", "PostToolUse"),
    ("session_start", "SessionStart"),
    ("user_prompt_submit", "UserPromptSubmit"),
)
# PostToolUse uses the nested matcher+hooks[] grouping (the Claude-schema shape
# both runners already accept for it); the other stages stay flat.
_NESTED_HOOK_STAGES = frozenset({"post_tool_use"})
_DEFAULT_HOOK_TIMEOUT = 5


def _require_codex_optout(item: dict, stage: str) -> None:
    """Enforce the documented Codex opt-out: an entry may omit ``codex_command``
    only when it declares ``codex_enabled: false`` plus a non-empty
    ``unsupported_reason`` — silent omission leaves no audit trail for why a
    hook is absent from ``.codex/hooks.json``."""
    reason = item.get("unsupported_reason")
    if (
        item.get("codex_enabled") is not False
        or not isinstance(reason, str)
        or not reason.strip()
    ):
        raise ValueError(
            f"hook `{item.get('id')}` ({stage}) omits `codex_command`; a "
            "Codex-skipped hook must declare `codex_enabled: false` and a "
            "non-empty `unsupported_reason`"
        )


# internal: fail-open wrapper injection. Every rendered tool-guard
# command is prefixed with the payload wrapper so a missing/unspawnable
# handler exits 0 (with hook_infra_failure telemetry) instead of fail-closing
# the harness. Contract entries may opt out of fail-open via
# ``fail_mode: closed`` (rendered as --fail-mode=closed; the wrapper then
# blocks on a missing handler). The transform lives in the stdlib-only
# sibling module ``_guard_wrap.py`` so ``check_harness_sync.py`` can import
# the SAME implementation without this module's pydantic dependency.


def _load_guard_wrap_module():
    import importlib.util

    path = Path(__file__).resolve().parent / "_guard_wrap.py"
    spec = importlib.util.spec_from_file_location("_payload_guard_wrap", path)
    if spec is None or spec.loader is None:  # pragma: no cover - ships together
        raise SystemExit(f"missing payload module {path}")
    module = importlib.util.module_from_spec(spec)
    spec.loader.exec_module(module)
    return module


_wrap_guard_command = _load_guard_wrap_module().wrap_guard_command


def _render_hooks_config(
    hooks_spec: dict,
    *,
    command_key: str,
    matcher_key: str,
    include_status_message: bool,
    enforce_codex_optout: bool = False,
) -> dict:
    config: dict[str, list[dict]] = {}
    for stage, config_stage in _HOOK_STAGE_KEYS:
        entries: list[dict] = []
        for item in hooks_spec.get(stage) or []:
            command = item.get(command_key)
            # strip-normalized so renderer and check_harness_sync classify a
            # whitespace-only command identically (omitted, not present).
            if not isinstance(command, str) or not command.strip():
                if enforce_codex_optout:
                    _require_codex_optout(item, stage)
                continue
            if enforce_codex_optout and item.get("codex_enabled") is False:
                raise ValueError(
                    f"hook `{item.get('id')}` ({stage}) declares "
                    "`codex_enabled: false` but still sets `codex_command`; "
                    "drop one of the two"
                )
            handler: dict = {
                "type": "command",
                "command": _wrap_guard_command(
                    command, fail_mode=item.get("fail_mode")
                ),
                "timeout": int(item.get("timeout", _DEFAULT_HOOK_TIMEOUT)),
            }
            if include_status_message:
                status_message = item.get("status_message")
                if not isinstance(status_message, str) or not status_message.strip():
                    raise ValueError(
                        f"hook `{item.get('id')}` ({stage}) is missing the non-empty "
                        "`status_message` required to render Codex statusMessage"
                    )
                handler["statusMessage"] = status_message
            matcher = item.get(matcher_key, item.get("matcher"))
            if stage in _NESTED_HOOK_STAGES:
                entries.append({"matcher": matcher, "hooks": [handler]})
            else:
                entry: dict = {}
                if matcher is not None:
                    entry["matcher"] = matcher
                entry.update(handler)
                entries.append(entry)
        config[config_stage] = entries
    return {"hooks": config}


