def _expected_hooks_outputs(repo_root: Path) -> dict[Path, str]:
    """Map generated hook-config paths to content for ``repo_root``.

    Empty when the repo has no harness contract (e.g. a consumer that does not
    materialize the contracts surface) so legacy callers stay unaffected.
    """
    contract_path = repo_root / _HOOKS_CONTRACT_RELATIVE
    if not contract_path.is_file():
        return {}
    contract = yaml.safe_load(contract_path.read_text()) or {}
    hooks_spec = contract.get("hooks")
    if not isinstance(hooks_spec, dict) or not hooks_spec:
        return {}
    outputs = {
        repo_root
        / ".github"
        / "hooks"
        / "terminal-guard.json": render_hooks_config_text(
            render_vscode_hooks_config(hooks_spec)
        ),
        repo_root / ".codex" / "hooks.json": render_hooks_config_text(
            render_codex_hooks_config(hooks_spec)
        ),
        repo_root / ".claude" / "settings.hooks.json": render_hooks_config_text(
            render_claude_hooks_config(hooks_spec)
        ),
    }
    # Overlay consumers materialize shared surfaces (e.g. .github/hooks) as
    # symlinks into the .workbay/remote clone; writing through one would
    # mutate the shared clone instead of the consumer tree. Those paths already
    # resolve to the package-generated golden, so they are skipped here.
    return {
        path: content
        for path, content in outputs.items()
        if not path.is_symlink() and not path.parent.is_symlink()
    }


def _write_outputs(outputs: dict[Path, str]) -> None:
    for path, content in outputs.items():
        path.parent.mkdir(parents=True, exist_ok=True)
        path.write_text(content)


def _extra_generated_files(root: Path, expected_paths: set[Path]) -> list[Path]:
    if not root.exists():
        return []
    extras: list[Path] = []
    for path in root.rglob("*"):
        if not path.is_file():
            continue
        if path in expected_paths:
            continue
        try:
            content = path.read_text()
        except OSError:
            continue
        if content.startswith(GENERATOR_TAG):
            extras.append(path)
    return sorted(extras)


def _check_outputs(
    outputs: dict[Path, str],
    claude_out: Path,
    prompts_out: Path,
    codex_router_out: Path,
) -> int:
    failures: list[str] = []
    for path, expected in outputs.items():
        if not path.exists():
            failures.append(f"missing generated file: {path}")
            continue
        actual = path.read_text()
        if actual != expected:
            failures.append(f"drift detected: {path}")

    expected_paths = set(outputs)
    # internal: claude_out is no longer manifest-driven (the
    # `.claude/commands/<id>.md` surface moved to the plugin tree), so
    # any tagged file under claude_out — e.g. the harness-only
    # `spec.md` — is intentionally outside this manifest's scope and
    # must not be flagged as stale here.
    extras = _extra_generated_files(
        prompts_out, expected_paths
    ) + _extra_generated_files(codex_router_out, expected_paths)
    for path in extras:
        failures.append(f"stale generated file not declared in manifest: {path}")

    if failures:
        print(
            "Agent workflow adapters are out of sync with the manifest:",
            file=sys.stderr,
        )
        for failure in failures:
            print(f"  - {failure}", file=sys.stderr)
        return 1
    print("✓ agent-workflows: generated adapters match the manifest.")
    return 0


def _load_mcp_servers_manifest(path: Path) -> dict:
    """Load and shallow-validate mcp_servers.yaml (internal input)."""
    try:
        payload = yaml.safe_load(path.read_text()) or {}
    except FileNotFoundError as exc:
        raise SystemExit(f"mcp_servers manifest not found: {path}") from exc
    if not isinstance(payload, dict):
        raise SystemExit(f"{path}: must parse to a mapping")
    if payload.get("version") != 2:
        raise SystemExit(
            f"{path}: must declare version=2; found {payload.get('version')!r}"
        )
    registration = payload.get("registration")
    if not isinstance(registration, dict) or not registration:
        raise SystemExit(
            f"{path}: version=2 requires a non-empty `registration` mapping "
            "(harness -> root|plugin); see implementation note"
        )
    plugin_capable = {h for h, _ in _PLUGIN_HARNESSES}
    for harness_name, owner in registration.items():
        if owner not in _MCP_REGISTRATION_OWNERS:
            raise SystemExit(
                f"{path}: registration.{harness_name} must be one of "
                f"{sorted(_MCP_REGISTRATION_OWNERS)}; found {owner!r}"
            )
        if owner == "plugin" and harness_name not in plugin_capable:
            raise SystemExit(
                f"{path}: registration.{harness_name}=plugin is unimplementable: "
                f"{harness_name!r} has no emitted plugin tree (plugin-capable "
                f"harnesses: {sorted(plugin_capable)}). Under plugin ownership "
                "the root surface stops registering the servers and no plugin "
                ".mcp.json exists to take over, so the harness silently loses "
                "all MCP registration (implementation note). Use registration: root."
            )
    missing = [h for h, _ in _PLUGIN_HARNESSES if h not in registration]
    if missing:
        raise SystemExit(
            f"{path}: registration must cover every plugin harness; missing {missing}"
        )
    plugin_version = payload.get("plugin_version")
    if not isinstance(plugin_version, str) or not plugin_version.strip():
        raise SystemExit(f"{path}: plugin_version must be a non-empty string")
    servers = payload.get("mcp_servers")
    if not isinstance(servers, list) or not servers:
        raise SystemExit(f"{path}: mcp_servers must be a non-empty list")
    for index, server in enumerate(servers):
        if not isinstance(server, dict):
            raise SystemExit(f"{path}: mcp_servers[{index}] must be a mapping")
        for field in ("name", "command"):
            value = server.get(field)
            if not isinstance(value, str) or not value.strip():
                raise SystemExit(
                    f"{path}: mcp_servers[{index}].{field} must be a non-empty string"
                )
        args = server.get("args", [])
        if not isinstance(args, list) or not all(isinstance(a, str) for a in args):
            raise SystemExit(
                f"{path}: mcp_servers[{index}].args must be a list of strings"
            )
        harnesses = server.get("harnesses", [])
        if not isinstance(harnesses, list) or not all(
            isinstance(h, str) for h in harnesses
        ):
            raise SystemExit(
                f"{path}: mcp_servers[{index}].harnesses must be a list of strings"
            )
        unregistered = sorted(set(harnesses) - set(registration))
        if unregistered:
            raise SystemExit(
                f"{path}: mcp_servers[{index}].harnesses {unregistered} missing from "
                "the `registration` ownership table; every server harness must "
                "declare root|plugin ownership (implementation note)"
            )
    return payload


def _render_mcp_servers_json(mcp_manifest: dict, harness: str) -> str:
    """Render the .mcp.json blob for the requested harness.

    Servers whose ``harnesses`` list excludes the harness are skipped.
    Output ordering follows the manifest insertion order; each entry
    carries ``"type": "stdio"`` to stay compatible with the bootstrap
    install path. Claude Code uses the camelCase ``mcpServers`` wrapper
    for plugin MCP config; Codex CLI 0.131.0 rejects wrapped plugin MCP
    config as an invalid server entry, so Codex receives the bare server
    map shape.
    """
    entries: dict[str, dict] = {}
    for server in mcp_manifest["mcp_servers"]:
        harnesses = server.get("harnesses") or []
        if harnesses and harness not in harnesses:
            continue
        entries[server["name"]] = {
            "type": "stdio",
            "command": server["command"],
            "args": list(server.get("args", [])),
        }
        env = server.get("env")
        if isinstance(env, dict) and env:
            entries[server["name"]]["env"] = dict(env)
    if harness in {"claude", "cursor", "grok"}:
        payload = {"mcpServers": entries}
    elif harness == "codex":
        payload = entries
    else:
        raise SystemExit(
            f"unsupported plugin harness for .mcp.json rendering: {harness!r}"
        )
    return json.dumps(payload, indent=2, ensure_ascii=False) + "\n"


def _render_plugin_manifest_json(
    plugin_version: str, *, include_mcp_servers: bool
) -> str:
    """Render the metadata-only plugin.json (live-doc verified shape).

    The body is identical across harnesses — only the parent directory
    name differs. ``include_mcp_servers`` is driven by the manifest's
    `registration` ownership table (implementation note): under ``root`` ownership
    the bootstrap-written root surface registers the MCP servers, so the
    plugin manifest omits ``mcpServers`` to avoid double-registration;
    under ``plugin`` ownership the manifest points at the sibling
    ``./.mcp.json``.
    """
    payload = {
        "name": _PLUGIN_NAME,
        "version": plugin_version,
        "description": _PLUGIN_DESCRIPTION,
        "skills": "./skills/",
    }
    if include_mcp_servers:
        payload["mcpServers"] = "./.mcp.json"
    return json.dumps(payload, indent=2, ensure_ascii=False) + "\n"


def _render_harness_routing_table(harness_protocol: dict) -> str:
    """Render the review-parallel routing table from harness-protocol.yaml."""
    capabilities = harness_protocol.get("harness_capabilities") or {}
    review_parallel = capabilities.get("review_parallel") or {}
    rows = review_parallel.get("rows") or []
    if not isinstance(rows, list) or not rows:
        raise SystemExit(
            "harness-protocol.yaml must declare harness_capabilities.review_parallel.rows"
        )
    lines = [
        "| Harness detected | Primitive the coordinator uses | Notes |",
        "| --- | --- | --- |",
    ]
    for row in rows:
        if not isinstance(row, dict):
            continue
        lines.append(f"| {row['label']} | {row['primitive']} | {row['notes']} |")
    return "\n".join(lines) + "\n"


def _inject_harness_routing_table(body: str, table: str) -> str:
    begin = body.find(_HARNESS_ROUTING_BEGIN)
    end = body.find(_HARNESS_ROUTING_END)
    if begin == -1 or end == -1 or end < begin:
        return body
    block_start = begin + len(_HARNESS_ROUTING_BEGIN)
    return body[:block_start] + "\n" + table + body[end:]


def _render_grok_plugin_hooks_json(harness_protocol: dict) -> str:
    """Project PreToolUse guards into the Grok plugin hooks surface.

    Only ``pre_tool_use`` rows are emitted here so plugin-delivered guards
    do not double-fire against compat-loaded ``.claude/settings.json`` hooks
    for post/session events after ``/hooks-trust``.
    """
    hook_rows = (harness_protocol.get("hooks") or {}).get("pre_tool_use") or []
    entries: list[dict[str, object]] = []
    for row in hook_rows:
        if not isinstance(row, dict):
            continue
        grok_command = row.get("grok_command")
        if not isinstance(grok_command, str) or not grok_command.strip():
            continue
        entries.append(
            {
                "matcher": row.get("matcher", ""),
                "hooks": [
                    {
                        "type": "command",
                        "command": _wrap_guard_command(
                            grok_command, fail_mode=row.get("fail_mode")
                        ),
                    }
                ],
            }
        )
    payload = {"hooks": {"PreToolUse": entries}}
    return json.dumps(payload, indent=2, ensure_ascii=False) + "\n"


def _render_plugin_skill(
    structured: dict, body: str, manifest: dict, harness_protocol: dict | None = None
) -> str:
    """Render a SKILL.md body for the plugin tree.

    ADR-001 mandates byte-identical SKILL.md across harnesses, so this
    intentionally drops the ``generator.claude_overrides`` block.
    """
    canonical, _ = _strip_generator_metadata(structured)
    fm_text = yaml.safe_dump(
        canonical, sort_keys=False, default_flow_style=False
    ).rstrip()
    bullets = _global_instruction_bullets(manifest)
    if bullets:
        body = "## Global Instructions\n\n" + "\n".join(bullets) + "\n\n" + body
    if harness_protocol is not None and _HARNESS_ROUTING_BEGIN in body:
        body = _inject_harness_routing_table(
            body, _render_harness_routing_table(harness_protocol)
        )
    body = body if body.endswith("\n") else body + "\n"
    return f"---\n{fm_text}\n---\n\n{body}"


def _sha256_digest(content: str) -> str:
    return f"sha256:{hashlib.sha256(content.encode('utf-8')).hexdigest()}"


